"use client";

import { useState } from "react";
import { motion } from "framer-motion";
import { Check, Copy } from "lucide-react";

import type { CreativeUniverse } from "@/types/creative-universe";
import {
  FORMAT_DIRECTIONS,
  FORMAT_LABELS,
  PRIORITY_STYLES,
  STAGE_LABELS,
} from "@/config/universe-ui";
import { riseItem, staggerContainer } from "@/lib/motion/variants";
import { LeafCard } from "@/components/motion/LeafCard";
import { OutcomeTracker } from "@/components/dashboard/OutcomeTracker";

/* ============================================================================
   <AngleCards /> — the top HYPOTHESES in full detail: the falsifiable
   statement, the psychology, the verbatim hook, the kill-metric prediction,
   and the outcome tracker that feeds the learning loop.
   ========================================================================== */

/** How many hypotheses get the full-card treatment (rest live in the matrix). */
const TOP_ANGLE_COUNT = 6;

/** Copy-to-clipboard for one hypothesis — paste-ready for Slack/Notion. */
function CopyBrief({ text }: { text: string }): React.JSX.Element {
  const [copied, setCopied] = useState(false);

  async function copy(): Promise<void> {
    try {
      await navigator.clipboard.writeText(text);
      setCopied(true);
      setTimeout(() => setCopied(false), 1600);
    } catch {
      /* clipboard unavailable (permissions/http) — silently ignore */
    }
  }

  return (
    <button
      type="button"
      onClick={() => void copy()}
      aria-label="Copy hypothesis brief"
      className="inline-flex cursor-pointer items-center gap-1.5 rounded-full px-2.5 py-1 font-mono text-[10px] tracking-[0.08em] text-ink-tertiary uppercase transition-colors duration-150 hover:bg-glass hover:text-ink-secondary print:hidden"
    >
      {copied ? (
        <>
          <Check className="size-3 text-accent" strokeWidth={2.5} /> Copied
        </>
      ) : (
        <>
          <Copy className="size-3" strokeWidth={2.25} /> Copy brief
        </>
      )}
    </button>
  );
}

/** Formats a hypothesis as a paste-ready brief. */
function briefText(angle: {
  title: string;
  statement: string;
  whyItWorks: string;
  hook: string;
  prediction: string;
}): string {
  return [
    `HYPOTHESIS: ${angle.title}`,
    `Statement: ${angle.statement}`,
    `Why it works: ${angle.whyItWorks}`,
    `Hook: "${angle.hook}"`,
    `Confirm if: ${angle.prediction}`,
    `— generated by Catalyst`,
  ].join("\n");
}

export function AngleCards({
  universe,
}: {
  universe: CreativeUniverse;
}): React.JSX.Element {
  const topAngles = [...universe.angles]
    .sort((a, b) => b.score - a.score)
    .slice(0, TOP_ANGLE_COUNT);

  return (
    <motion.section
      variants={staggerContainer}
      initial="hidden"
      whileInView="visible"
      viewport={{ once: true, margin: "-80px" }}
      aria-labelledby="angles-heading"
    >
      <motion.div variants={riseItem} className="mb-6">
        <h2 id="angles-heading" className="machine-label">
          04 · Creative hypotheses — falsifiable, prioritized, shootable
        </h2>
        {/* Plain-words legend — scores and labels must never be a mystery */}
        <p className="mt-2.5 max-w-3xl text-[13px] leading-relaxed text-ink-tertiary">
          <span className="font-medium text-ink-secondary">How to read this:</span>{" "}
          the score (0–100) is the engine&apos;s confidence this idea beats what
          you&apos;re running now — based on how well it fits your buyers&apos;
          psychology and how little the competition uses it.{" "}
          <span className="font-mono text-[11px] uppercase">Scale</span> = proven
          pattern, put money on it · <span className="font-mono text-[11px] uppercase">Test</span> =
          high upside, unproven · <span className="font-mono text-[11px] uppercase">Iterate</span> =
          refine after a winner · <span className="font-mono text-[11px] uppercase">Watch</span> =
          save for later.
        </p>
      </motion.div>

      <div className="grid gap-5 md:grid-cols-2 xl:grid-cols-3">
        {topAngles.map((angle, index) => (
          <LeafCard key={angle.title} index={index % 3}>
            <article className="glass-panel flex h-full flex-col rounded-[--radius-panel] p-6">
            {/* Rank + score header */}
            <div className="mb-3 flex items-center justify-between">
              <span className="flex items-center gap-2 font-mono text-xs text-data">
                {String(index + 1).padStart(2, "0")}
                <CopyBrief text={briefText(angle)} />
              </span>
              <div className="flex items-center gap-2">
                <span className="font-mono text-sm font-medium text-ink">
                  {angle.score}
                </span>
                <span
                  className={`rounded-full px-2.5 py-1 font-mono text-[10px] tracking-[0.1em] uppercase ${PRIORITY_STYLES[angle.testingPriority]}`}
                >
                  {angle.testingPriority}
                </span>
              </div>
            </div>

            {/* Identity chips FIRST — what kind of angle this is, at a glance */}
            <div className="mb-3 flex flex-wrap gap-1.5">
              <span className="rounded-full bg-accent-ghost px-2.5 py-1 font-mono text-[10px] tracking-[0.08em] text-accent-deep uppercase">
                {STAGE_LABELS[angle.awarenessStage]}
              </span>
              <span className="rounded-full border border-line px-2.5 py-1 font-mono text-[10px] tracking-[0.08em] text-ink-secondary uppercase">
                {FORMAT_LABELS[angle.format]}
              </span>
              <span className="rounded-full border border-line px-2.5 py-1 font-mono text-[10px] tracking-[0.08em] text-ink-tertiary uppercase">
                {angle.emotion}
              </span>
            </div>

            <h3 className="text-[17px] leading-snug font-semibold text-ink">
              {angle.title}
            </h3>

            {/* The falsifiable statement — what beats what, for whom */}
            <p className="mt-2.5 text-sm leading-relaxed font-medium text-ink">
              {angle.statement}
            </p>
            <p className="mt-2 text-sm leading-relaxed text-ink-secondary">
              {angle.whyItWorks}
            </p>

            {/* The hook — editorial voice, ready to read aloud */}
            <blockquote className="editorial mt-4 border-l-2 border-accent pl-4 text-[17px] leading-snug text-ink">
              “{angle.hook}”
            </blockquote>

            {/* What physically gets produced */}
            <p className="mt-3 text-[13px] leading-relaxed font-medium text-ink-secondary">
              <span className="machine-label mr-1.5">Shoot</span>
              {FORMAT_DIRECTIONS[angle.format]}
            </p>

            {/* The kill-metric — how this hypothesis gets proven or killed */}
            <p className="mt-3 rounded-xl bg-void px-3.5 py-2.5 font-mono text-xs leading-relaxed text-ink-secondary">
              Confirm if: {angle.prediction}
            </p>

            <div className="mt-auto" />

            {/* Learning loop — mark what actually happened */}
            <OutcomeTracker hypothesisTitle={angle.title} />
            </article>
          </LeafCard>
        ))}
      </div>
    </motion.section>
  );
}
